"""
转换：
int(x)
float(x)
str(x)
"""
num_str = str(11)
print(type(num_str),num_str)

float_str = str(11.11)
print(type(float_str),float_str)

str_int = int('11')
print(type(str_int),str_int)

int_float = int(11.11)
print(type(int_float),int_float)

num = float(11)
print(type(num),num)

num1 = float("11.111")
print(type(num1),num1)

